<div id="about-the-game" class="row clearfix">
    <div class="header"><h1>What is Seed of Andromeda?</h1></div>
    <div class="col double-col-2">
        <div class="text">
                Seed of Andromeda is a Voxel based Sandbox RPG in a futuristic setting.
                The player will crash land on a fully round voxel planet and will be
                forced to survive hostile creatures. As the player progresses through
                the game, he will be able to form settlements with NPC survivors. Once
                the player has progressed enough along the technology path, he will be
                able to create spaceships so that he may explore the other planets of
                the solar system, and salvage the wreckage of his fleet in orbit. The
                game will emphasize customization and moddability, so there will be
                many tools released with the game including a world editor, tree
                editor, biome editor, and block editor. The world editor allows players
                to create full procedural worlds that they may play with and share with
                others, and the content for these worlds can be created using the other
                editor tools. SoA is heavily inspired by Minecraft, Starforge,
                Starmade, Vox, and most other voxel games.
            </div>
