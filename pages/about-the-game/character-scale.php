<div id="about-the-game" class="row clearfix">
    <div class="header"><h1>Character Progressions</h1></div>
    <div class="col double-col-2">
        <div class="text">
                Characters in Seed of Andromeda don't just stay static in how proficient they are at performing actions. The game incorporates a 
                fully-fledged progression system, aimed at enabling characters in progressing in all tasks they carry out; from running to shooting 
                to cooking the perfect casserole! The system is aimed at mimicking how we progress in real life, and so avoids arbitrary levelling of 
                many games!
            </div>
    </div>
</div>
<div id="about-the-game" class="row clearfix">
    <div class="header"><h1>Technologically Focused</h1></div>
    <div class="col double-col-2">
        <div class="text">
                Seed of Andromeda also has a fully-realised technology tree and research mechanic for you, and other inhabitants of the game's worlds, to 
                progress along and with. Always wanted that ray gun your mum promised for your birthday? Well get researching and engineering wondrous 
                technologies! Don't want to while away in a lab? Then get out and explore, find alien crash sites and learn from the past! Don't much 
                like the idea of trekking through the wilderness? Butter up some NPCs with flattery and sweet talk and they might divulge some of their 
                hard earned technologies! Or just blow them up and steal it... it's all up to you!
            </div>
    </div>
</div>
<div id="about-the-game" class="row clearfix">
    <div class="header"><h1>Cooperation is (Maybe) King!</h1></div>
    <div class="col double-col-2">
        <div class="text">
                Getting the resources to achieve massive feats like flying into space is no easy job. For this off unto you the tools of cooperation! You 
                can work alongside NPCs to complete many tasks, from mining to farming to wiping out another faction. Want yourself tonnes of metal to create 
                that star ship you've dreamed of? Get a group of NPCs to operate mines for you, or find NPCs already operating mines and steal their precious 
                metals!
            </div>
    </div>
</div>
<div id="about-the-game" class="row clearfix">
    <div class="header"><h1>Respect!</h1></div>
    <div class="col double-col-2">
        <div class="text">
                The choices you make, however, have a great impact on how the world you inhabit evolves; that mine you robbed may be backed by a large colony who 
